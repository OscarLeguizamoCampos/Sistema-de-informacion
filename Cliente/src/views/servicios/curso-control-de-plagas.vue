            <p>El combate de los insectos se debe hacer especialmente y con severidad en el semillero y en el vivero, pues en estos lugares tienen poca importancia los insectos beneficiosos y, como el área de aplicación es restringida, no afecta las zonas de producción. Las plantitas deben salir al campo libres de insectos o daños ocasionados por ellos. Para combatir las plagas en el vivero es recomendable establecer un programa preventivo de sanidad.</p>

            <p>Quizás lo más práctico es hacer tratamientos semanales y, si no hay problemas severos, pueden distanciarse los tratamientos a dos o tres semanas. Los problemas con defoliadores, escolítidos, áfidos y trips se pueden combatir aplicando químicos de amplio espectro, tales como methomyl (Lannate), endosulfan (Thiodan), oxydemeton-methyl (Metasystox-R) u otros productos.</p>

